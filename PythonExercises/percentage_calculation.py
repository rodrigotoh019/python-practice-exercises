# sample of percentage calculation

projected_total_sales = int(input('What''s the projected amount of total sales this year? : \t'))
percent = 0.23 * projected_total_sales
print(f'The 23% of the projected amount of {projected_total_sales} this year is : {percent:.2f}')

# using the user's input in projected_total_sales as a base of 100% then multiplying it to the desired percentage with the percent formula
