# practicing the > < and ==

num1 = int(input('Enter a number #1: '))
num2 = int(input('Enter a number #2: '))
if num1 > num2:
    print('The first number is greater than the second')
elif num1 < num2:
    print('The second number is greater than the first')
elif num1 == num2:
    print('Both numbers are equal')